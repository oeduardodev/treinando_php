<?php 
$valor = 20;

//Pré-incremento
echo "<br><p>Pré-incremento:</p>";
echo ++$valor;

//Pós-incremento
echo "<br><p>Pós-incremento:</p>";
echo $valor++;


//Pré-decremento
echo "<br><p>Pré-incremento:</p>";
echo --$valor;

//Pós-decremento
echo "<br><p>Pós-decremento:</p>";
echo $valor--;

//Valor Original
echo "<br><p>Valor Original:</p>";
echo $valor;
?>